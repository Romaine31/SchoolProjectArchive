public class CircleMethods {
    public double computeArea(double x){
        double z = x*x;
        return 3.14*z;
    }
    public double computeCircumference(double x){
        return 2*3.14*x;
    }
    public double computeDiameter(double x){
        return 2*x;
    }
}
