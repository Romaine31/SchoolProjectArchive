public class overloadingsub {
    private int x;
    private double y;
    overloadingsub(int z,double t){
        x=z*z;
        y=t*t;
    }//JOHN ROMAINE CAGUBCUB
    public int getint (){
        return this.x;
    }
    public double getdouble () {
        return this.y;
    }
}
